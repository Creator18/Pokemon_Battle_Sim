class DatabaseLayer:

    def __init__(self, engine, session_factory):
        self._engine          = engine
        self._session_factory = session_factory

    @classmethod
    async def create(cls, db_url: str = DATABASE_URL):
        engine = create_async_engine(db_url, echo=False)

        @event.listens_for(engine.sync_engine, "connect")
        def set_sqlite_pragma(dbapi_conn, _):
            cursor = dbapi_conn.cursor()
            cursor.execute("PRAGMA journal_mode=WAL")
            cursor.close()

        async with engine.begin() as conn:
            await conn.run_sync(Base.metadata.create_all)

        factory = async_sessionmaker(engine, expire_on_commit=False)
        return cls(engine, factory)

    # ── Session lifecycle ─────────────────────

    async def new_session(self, session_id: str) -> dict:
        state = _default_state(session_id)
        async with self._session_factory() as session:
            async with session.begin():
                # Wipe existing data
                for table in (BattleLogRow, TurnResultRow,
                              DeclarationRow, TerrainRow,
                              PokemonRow, SessionRow):
                    await session.execute(
                        delete(table).where(
                            table.session_id == session_id))

                session.add(SessionRow(
                    session_id=session_id,
                    phase=GamePhase.WAITING.value,
                    turn_number=0, battle_over=False, winner=None))
                await session.flush()

                for pid in (1, 2):
                    pd = state["pokemon"][f"p{pid}"]
                    tq, tr = pd["tile"]
                    session.add(PokemonRow(
                        session_id=session_id, player_id=pid,
                        name=pd["name"], level=pd["level"],
                        tile_q=tq, tile_r=tr,
                        max_hp=pd["max_hp"], current_hp=pd["current_hp"],
                        attack=pd["attack"], defense=pd["defense"],
                        sp_atk=pd["sp_atk"], sp_def=pd["sp_def"],
                        base_speed=pd["base_speed"], status=pd["status"],
                        can_pass_through=pd["can_pass_through"],
                        stat_stages_json=json.dumps(pd["stat_stages"]),
                        boost_mult=pd["boost_mult"],
                        tiles_short=pd["tiles_short"],
                        straight_tiles=pd["straight_tiles"],
                        cooldowns_json=json.dumps(pd["cooldowns"]),
                        path_this_turn_json=json.dumps(pd["path_this_turn"]),
                        position_history_json=json.dumps(pd["position_history"]),
                        moves_json=json.dumps(pd["moves"]),
                    ))

                # Store initial battlefield terrain
                for entry in state["terrain"]:
                    tq, tr = entry["tile"]
                    session.add(TerrainRow(
                        session_id=session_id,
                        tile_q=tq, tile_r=tr,
                        terrain_type=entry["terrain_type"],
                        owner=entry["owner"],
                        turns_left=entry["turns_left"],
                        integrity=entry["integrity"]))

        return state

