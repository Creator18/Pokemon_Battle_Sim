def qa_attack_priority(base_speed: int, order: "ActionOrder") -> int:
    if order == ActionOrder.MOVE_FIRST:
        return 9999 + QA_MOVE_FIRST_PRIORITY_BUMP
    return 9999

def qa_power_multiplier(tiles_moved: int, order: "ActionOrder") -> float:
    if order == ActionOrder.ATTACK_FIRST:
        return QA_ATTACK_FIRST_POWER_MULT
    return max(QA_MOVE_FIRST_POWER_MIN,
               1.0 - tiles_moved * QA_MOVE_FIRST_POWER_PENALTY)

def qa_check_straight_line(attacker_tile, defender_tile, barrier_tiles, grid=None):
    if grid is None: grid = HEX_GRID
    line = hex_line_tiles(*attacker_tile, *defender_tile)
    for tile in line[1:]:
        if tile in barrier_tiles: return False, line
    return True, line

def qa_landing_tile(attacker_tile, defender_tile, blocked_tiles, occupied_tiles, grid=None):
    if grid is None: grid = HEX_GRID
    candidates = [
        (nq, nr) for nq, nr in hex_neighbors(*defender_tile)
        if (nq, nr) in grid and (nq, nr) not in blocked_tiles
        and (nq, nr) not in occupied_tiles
    ]
    if not candidates: return None
    return min(candidates, key=lambda t: hex_distance(*attacker_tile, *t))


