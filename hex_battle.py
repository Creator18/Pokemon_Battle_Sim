def get_ring(cq, cr, radius, grid=None):
    if grid is None: grid = HEX_GRID
    if radius == 0:
        return {(cq, cr)} if (cq, cr) in grid else set()
    results = set()
    q = cq + AXIAL_DIRECTIONS[4][0] * radius
    r = cr + AXIAL_DIRECTIONS[4][1] * radius
    for i in range(6):
        for _ in range(radius):
            if (q, r) in grid: results.add((q, r))
            dq, dr = AXIAL_DIRECTIONS[i]
            q += dq; r += dr
    return results

def get_hex_area(cq, cr, radius, grid=None):
    if grid is None: grid = HEX_GRID
    result = set()
    for rad in range(radius + 1):
        result |= get_ring(cq, cr, rad, grid)
    if (cq, cr) in grid: result.add((cq, cr))
    return result

def get_directional_cone(origin_q, origin_r, direction_vec, length, grid=None):
    if grid is None: grid = HEX_GRID
    dq, dr = direction_vec
    tiles = set()
    q, r = origin_q + dq, origin_r + dr
    for _ in range(length):
        if (q, r) in grid: tiles.add((q, r))
        q += dq; r += dr
    return tiles

