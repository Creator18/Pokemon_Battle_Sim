def get_attack_direction(from_q, from_r, to_q, to_r):
    dq, dr = to_q - from_q, to_r - from_r
    if (dq, dr) in AXIAL_DIRECTIONS: return (dq, dr)
    target_angle = math.atan2(dr, dq)
    best_dir = AXIAL_DIRECTIONS[0]; best_diff = float("inf")
    for d in AXIAL_DIRECTIONS:
        angle = math.atan2(d[1], d[0])
        diff = abs(math.atan2(math.sin(target_angle - angle), math.cos(target_angle - angle)))
        if diff < best_diff: best_diff = diff; best_dir = d
    return best_dir

