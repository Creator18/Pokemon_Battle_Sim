def axial_to_cube(q, r):
    return q, -q - r, r

def cube_to_axial(x, y, z):
    return x, z

def cube_round(x, y, z):
    rx, ry, rz = round(x), round(y), round(z)
    dx, dy, dz = abs(rx - x), abs(ry - y), abs(rz - z)
    if dx > dy and dx > dz:   rx = -ry - rz
    elif dy > dz:              ry = -rx - rz
    else:                      rz = -rx - ry
    return rx, ry, rz

def hex_distance(q1, r1, q2, r2):
    x1, y1, z1 = axial_to_cube(q1, r1)
    x2, y2, z2 = axial_to_cube(q2, r2)
    return max(abs(x1 - x2), abs(y1 - y2), abs(z1 - z2))

AXIAL_DIRECTIONS = [(1, 0), (1, -1), (0, -1), (-1, 0), (-1, 1), (0, 1)]

def hex_neighbors(q, r):
    return [(q + dq, r + dr) for dq, dr in AXIAL_DIRECTIONS]

def hex_neighbors_in_grid(q, r, grid_set):
    return [(nq, nr) for nq, nr in hex_neighbors(q, r)
            if (nq, nr) in grid_set]

