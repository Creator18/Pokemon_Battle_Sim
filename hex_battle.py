# ─────────────────────────────────────────────
# SESSION RECORD
# In-memory coordination state for one session.
# The authoritative game state lives in the DB.
# This holds: who is connected, who has declared,
# timestamps for timeout.
# ─────────────────────────────────────────────

@dataclass
class SessionRecord:
    session_id       : str
    created_at       : datetime = field(
        default_factory=lambda: datetime.now(timezone.utc))
    last_activity    : datetime = field(
        default_factory=lambda: datetime.now(timezone.utc))
    player_connected : dict = field(
        default_factory=lambda: {1: False, 2: False})
    player_reserved  : dict = field(
        default_factory=lambda: {1: False, 2: False})
    player_declared  : dict = field(
        default_factory=lambda: {1: False, 2: False})
    forfeit_player   : Optional[int] = None
    lock             : asyncio.Lock = field(
        default_factory=asyncio.Lock)

    # ── Queries ──────────────────────────────

    @property
    def both_connected(self) -> bool:
        return (self.player_connected[1]
                and self.player_connected[2])

    @property
    def both_declared(self) -> bool:
        return (self.player_declared[1]
                and self.player_declared[2])

    @property
    def player_count(self) -> int:
        return sum(1 for v in self.player_connected.values()
                   if v)

    @property
    def is_full(self) -> bool:
        return (self.player_reserved[1]
                and self.player_reserved[2])

    @property
    def available_player_id(self) -> Optional[int]:
        for pid in (1, 2):
            if not self.player_reserved[pid]:
                return pid
        return None

    def is_timed_out(self,
                     timeout_seconds: int = SESSION_TIMEOUT_S
                     ) -> bool:
        delta = datetime.now(timezone.utc) - self.last_activity
        return delta.total_seconds() > timeout_seconds

    # ── Mutations ────────────────────────────

    def touch(self):
        self.last_activity = datetime.now(timezone.utc)

    def connect_player(self, player_id: int):
        self.player_connected[player_id] = True
        self.touch()

    def disconnect_player(self, player_id: int):
        self.player_connected[player_id] = False
        self.touch()

    def set_declared(self, player_id: int):
        self.player_declared[player_id] = True
        self.touch()

    def clear_declarations(self):
        self.player_declared[1] = False
        self.player_declared[2] = False

    # ── Serialization ────────────────────────

    def to_status_dict(self) -> dict:
        return {
            "session_id"     : self.session_id,
            "player_count"   : self.player_count,
            "both_connected" : self.both_connected,
            "both_declared"  : self.both_declared,
            "p1_connected"   : self.player_connected[1],
            "p2_connected"   : self.player_connected[2],
            "p1_declared"    : self.player_declared[1],
            "p2_declared"    : self.player_declared[2],
            "forfeit_player" : self.forfeit_player,
            "created_at"     : self.created_at.isoformat(),
            "last_activity"  : self.last_activity.isoformat(),
        }

    def __repr__(self) -> str:
        p1 = '✓' if self.player_connected[1] else '✗'
        p2 = '✓' if self.player_connected[2] else '✗'
        d1 = '✓' if self.player_declared[1] else '✗'
        d2 = '✓' if self.player_declared[2] else '✗'
        return (f"SessionRecord({self.session_id} | "
                f"P1={p1} P2={p2} | decl P1={d1} P2={d2})")


