def generate_hex_grid(radius):
    tiles = set()
    for q in range(-radius, radius + 1):
        for r in range(-radius, radius + 1):
            if abs(q) + abs(r) + abs(-q - r) <= 2 * radius:
                tiles.add((q, r))
    return tiles

