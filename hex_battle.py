class TerrainManager:
    """
    Manages all terrain entities on the battlefield.
    Provides spatial lookups and bulk operations.
    """

    def __init__(self):
        self._entities: dict[tuple, TerrainEntity] = {}

    # ── Add / Remove ──────────────────────────

    def add_terrain(self, tile: tuple, terrain_type: TerrainType,
                    owner: int = 0, duration: int = None,
                    integrity: int = None):
        props = TERRAIN_PROPS.get(terrain_type,
                TERRAIN_PROPS[TerrainType.SLOW_ZONE])
        dur = duration if duration is not None else props.default_duration
        hp  = integrity if integrity is not None else props.default_integrity
        entity = TerrainEntity(
            tile=tile, terrain_type=terrain_type,
            owner=owner, turns_left=dur, integrity=hp)
        self._entities[tile] = entity
        return entity

    def remove_terrain(self, tile: tuple) -> Optional[TerrainEntity]:
        return self._entities.pop(tile, None)

    def has_terrain(self, tile: tuple) -> bool:
        return tile in self._entities

    def get_terrain(self, tile: tuple) -> Optional[TerrainEntity]:
        return self._entities.get(tile)

    # ── Bulk operations ───────────────────────

    def tick_all(self) -> list:
        """
        Tick all terrain, remove expired/destroyed.
        Returns list of (tile, type_name) that were removed.
        Permanent terrain (ROCK) never expires.
        TREE only removed if destroyed (integrity <= 0).
        """
        removed = []
        to_remove = []
        for tile, entity in self._entities.items():
            entity.tick()
            if entity.is_destroyed:
                to_remove.append(tile)
                removed.append((tile, entity.terrain_type.value))
            elif entity.is_expired and not entity.is_permanent:
                to_remove.append(tile)
                removed.append((tile, entity.terrain_type.value))
        for tile in to_remove:
            del self._entities[tile]
        return removed

    def damage_terrain_at(self, tile: tuple,
                          amount: int = 1) -> list:
        """
        Damage terrain at a tile. If destroyed, remove it.
        Returns list of destroyed terrain type names.
        """
        entity = self._entities.get(tile)
        if not entity:
            return []
        destroyed = entity.take_damage(amount)
        if destroyed:
            del self._entities[tile]
            return [entity.terrain_type.value]
        return []

    # ── Spatial lookups ───────────────────────

    @property
    def blocked_movement_tiles(self) -> set:
        """Tiles that block movement (BARRIER, TREE)."""
        return {tile for tile, entity in self._entities.items()
                if entity.blocks_movement}

    @property
    def blocked_los_tiles(self) -> set:
        """Tiles that block line of sight (BARRIER, TREE)."""
        return {tile for tile, entity in self._entities.items()
                if entity.blocks_los}

    @property
    def slow_tiles(self) -> set:
        """Tiles with step cost > 1 (ROCK)."""
        return {tile for tile, entity in self._entities.items()
                if entity.step_cost > DEFAULT_STEP_COST}

    @property
    def stat_hazard_tiles(self) -> set:
        """Tiles that apply stat stage changes on entry (SLOW_ZONE)."""
        return {tile for tile, entity in self._entities.items()
                if entity.is_stat_hazard}

    def get_step_cost(self, tile: tuple) -> int:
        """Step cost at a tile. DEFAULT_STEP_COST (1) if no terrain."""
        entity = self._entities.get(tile)
        if entity:
            return entity.step_cost
        return DEFAULT_STEP_COST

    def get_stat_effect(self, tile: tuple) -> Optional[dict]:
        """Stat stage effect at a tile. None if no effect."""
        entity = self._entities.get(tile)
        if entity and entity.is_stat_hazard:
            return entity.stat_effect
        return None

    def get_speed_modifier(self, tile: tuple) -> float:
        """
        Speed modifier at a tile. 1.0 if no terrain.
        NOTE: With the stat stage system, this is mainly used
        for legacy compatibility. New code should use
        get_step_cost() and get_stat_effect() instead.
        """
        entity = self._entities.get(tile)
        if entity:
            return entity.speed_modifier
        return 1.0

    # ── Contiguous field detection ────────────

    def get_contiguous_field(self, start_tile: tuple,
                             terrain_type: TerrainType = None) -> set:
        """
        Find all tiles in a contiguous field of the same terrain type
        connected to start_tile via adjacency.
        Used for entry penalty: only charge stat effect once per field.
        """
        entity = self._entities.get(start_tile)
        if not entity:
            return set()
        if terrain_type is None:
            terrain_type = entity.terrain_type

        visited = set()
        frontier = [start_tile]
        while frontier:
            tile = frontier.pop()
            if tile in visited:
                continue
            ent = self._entities.get(tile)
            if not ent or ent.terrain_type != terrain_type:
                continue
            visited.add(tile)
            for nb in hex_neighbors(*tile):
                if nb not in visited:
                    frontier.append(nb)
        return visited

    # ── Serialization ─────────────────────────

    def to_list(self) -> list:
        return [e.to_dict() for e in self._entities.values()]

    @classmethod
    def from_list(cls, data: list) -> "TerrainManager":
        tm = cls()
        for entry in data:
            entity = TerrainEntity.from_dict(entry)
            tm._entities[entity.tile] = entity
        return tm

    # ── Debug ─────────────────────────────────

    def __repr__(self) -> str:
        return f"TerrainManager({len(self._entities)} entities)"

    def debug_print(self):
        print(f"[Terrain] {len(self._entities)} entities:")
        for tile, entity in sorted(self._entities.items()):
            print(f"  {entity}")



