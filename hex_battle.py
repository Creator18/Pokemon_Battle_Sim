def count_straight_approach(path, attack_direction_vec):
    if len(path) < 2: return 0
    count = 0
    for i in range(len(path) - 1, 0, -1):
        dq = path[i][0] - path[i - 1][0]
        dr = path[i][1] - path[i - 1][1]
        if (dq, dr) == attack_direction_vec: count += 1
        else: break
    return count

