    # ── Load ──────────────────────────────────

    async def load_session(self, session_id: str) -> dict:
        async with self._session_factory() as session:
            sess_row = await session.get(SessionRow, session_id)
            if sess_row is None:
                raise KeyError(f"Session '{session_id}' not found in DB.")

            p_result = await session.execute(
                select(PokemonRow)
                .where(PokemonRow.session_id == session_id)
                .order_by(PokemonRow.player_id))
            p_rows = {r.player_id: r for r in p_result.scalars().all()}

            t_result = await session.execute(
                select(TerrainRow).where(TerrainRow.session_id == session_id))
            t_rows = t_result.scalars().all()

            d_result = await session.execute(
                select(DeclarationRow).where(DeclarationRow.session_id == session_id))
            d_rows = {r.player_id: r for r in d_result.scalars().all()}

            r_result = await session.execute(
                select(TurnResultRow).where(
                    TurnResultRow.session_id == session_id,
                    TurnResultRow.turn_number == sess_row.turn_number))
            r_rows = {r.player_id: r for r in r_result.scalars().all()}

            log_result = await session.execute(
                select(BattleLogRow.line)
                .where(BattleLogRow.session_id == session_id)
                .order_by(BattleLogRow.id).limit(60))
            log_lines = [row[0] for row in log_result.all()]

        state = {
            "meta": {
                "session_id": session_id, "version": 1,
                "battle_over": sess_row.battle_over,
                "winner": sess_row.winner,
                "turn_number": sess_row.turn_number,
                "phase": sess_row.phase,
            },
            "pokemon": {}, "terrain": [],
            "declarations": {"p1": None, "p2": None},
            "last_turn_results": {"p1": None, "p2": None},
            "battle_log": log_lines,
        }

        for pid in (1, 2):
            key = f"p{pid}"
            if pid not in p_rows:
                state["pokemon"][key] = _default_pikachu_dict(pid)
                continue
            row = p_rows[pid]
            state["pokemon"][key] = {
                "player_id": pid, "name": row.name,
                "tile": [row.tile_q, row.tile_r],
                "moves": json.loads(row.moves_json),
                "max_hp": row.max_hp, "current_hp": row.current_hp,
                "attack": row.attack, "defense": row.defense,
                "sp_atk": row.sp_atk, "sp_def": row.sp_def,
                "base_speed": row.base_speed, "level": row.level,
                "status": row.status,
                "can_pass_through": row.can_pass_through,
                "stat_stages": json.loads(row.stat_stages_json),
                "boost_mult": row.boost_mult,
                "tiles_short": row.tiles_short,
                "straight_tiles": row.straight_tiles,
                "cooldowns": json.loads(row.cooldowns_json),
                "path_this_turn": json.loads(row.path_this_turn_json),
                "position_history": json.loads(row.position_history_json),
                "declaration": None,
            }

        for pid, row in d_rows.items():
            key = f"p{pid}"
            if key not in state["pokemon"]: continue
            state["pokemon"][key]["declaration"] = {
                "move_name": row.move_name,
                "action_order": row.action_order,
                "target_tile": (json.loads(row.target_tile_json)
                                if row.target_tile_json else None),
                "planned_path": json.loads(row.planned_path_json),
            }
            state["declarations"][key] = state["pokemon"][key]["declaration"]

        state["terrain"] = [
            {
                "tile": [row.tile_q, row.tile_r],
                "terrain_type": row.terrain_type,
                "owner": row.owner,
                "turns_left": row.turns_left,
                "integrity": row.integrity,
            }
            for row in t_rows
        ]

        for pid, row in r_rows.items():
            state["last_turn_results"][f"p{pid}"] = json.loads(row.result_json)

        return state

