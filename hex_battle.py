def get_reachable_tiles(start_q, start_r, move_range,
                        blocked_tiles, occupied_tiles, grid=None):
    if grid is None: grid = HEX_GRID
    visited  = {(start_q, start_r): 0}
    frontier = [(start_q, start_r, 0)]
    reachable = set()
    while frontier:
        q, r, steps = frontier.pop(0)
        for nq, nr in hex_neighbors(q, r):
            if (nq, nr) not in grid: continue
            if (nq, nr) in blocked_tiles: continue
            new_steps = steps + 1
            if new_steps > move_range: continue
            if (nq, nr) in visited and visited[(nq, nr)] <= new_steps: continue
            visited[(nq, nr)] = new_steps
            if (nq, nr) not in occupied_tiles: reachable.add((nq, nr))
            frontier.append((nq, nr, new_steps))
    return reachable

