def _lerp(a, b, t):
    return a + (b - a) * t

def hex_line_tiles(q1, r1, q2, r2):
    n = hex_distance(q1, r1, q2, r2)
    if n == 0:
        return [(q1, r1)]
    x1, y1, z1 = axial_to_cube(q1, r1)
    x2, y2, z2 = axial_to_cube(q2, r2)
    tiles = []
    for i in range(n + 1):
        t = i / n
        rx, ry, rz = cube_round(
            _lerp(x1, x2, t), _lerp(y1, y2, t), _lerp(z1, z2, t))
        tiles.append(cube_to_axial(rx, ry, rz))
    return tiles

