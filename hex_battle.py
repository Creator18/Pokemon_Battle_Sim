def is_valid_path(path, blocked_tiles, occupied_tiles, move_range, grid=None):
    if grid is None: grid = HEX_GRID
    if not path: return False, "Empty path"
    if len(path) - 1 > move_range: return False, "Path too long"
    for i in range(1, len(path)):
        prev, curr = path[i - 1], path[i]
        if curr not in grid: return False, f"Tile {curr} not in grid"
        if curr in blocked_tiles: return False, f"Tile {curr} is blocked"
        if curr in occupied_tiles and i == len(path) - 1: return False, "Destination occupied"
        if hex_distance(*prev, *curr) != 1: return False, "Non-adjacent step"
    return True, "OK"

