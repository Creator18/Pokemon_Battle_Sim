# ─────────────────────────────────────────────
# BATTLE HANDLER
# ─────────────────────────────────────────────

class BattleHandler:

    def __init__(self, session_id: str,
                 db: "DatabaseLayer",
                 sm: "SessionManager",
                 cm: "ConnectionManager",
                 te: "TurnEngine"):
        self.session_id = session_id
        self._db = db
        self._sm = sm
        self._cm = cm
        self._te = te

    # ── Join ──────────────────────────────────

    async def handle_join(self, conn: WebSocketConnection,
                          player_id: int):
        await self._cm.register(conn)

        rec = await self._sm.get_session(self.session_id)
        should_start = False
        should_resync = False
        async with rec.lock:
            rec.connect_player(player_id)

            state    = await self._db.load_session(
                self.session_id)
            snapshot = self._db.build_render_snapshot(state)

            await conn.send_json(
                msg_joined(player_id, snapshot))

            if not rec.both_connected:
                await conn.send_json(msg_waiting(player_id))
            else:
                phase = state["meta"]["phase"]
                turn  = state["meta"]["turn_number"]

                if phase == GamePhase.WAITING.value and turn == 0:
                    # First time both connected — start the battle
                    await self._cm.broadcast(
                        self.session_id,
                        msg_both_ready(snapshot))
                    should_start = True
                else:
                    # Reconnection after lobby→game transition
                    # or mid-game reconnect. Don't start a new turn.
                    # Just send current state to the reconnecting player.
                    should_resync = True

        if should_start:
            await self._start_turn()
        elif should_resync:
            # Send current state so reconnecting client is up to date
            state    = await self._db.load_session(self.session_id)
            snapshot = self._db.build_render_snapshot(state)
            await self._cm.broadcast(
                self.session_id,
                msg_both_ready(snapshot))
            # If we're in DECLARING phase, send TURN_START so client
            # enters the declaration flow
            phase = state["meta"]["phase"]
            if phase == GamePhase.DECLARING.value:
                await self._cm.broadcast(
                    self.session_id,
                    msg_turn_start(
                        state["meta"]["turn_number"], snapshot))

    # ── Message dispatch ──────────────────────

    async def handle_message(self, conn: WebSocketConnection,
                             msg: dict):
        await self._sm.touch(self.session_id)
        msg_type = msg.get("type", "")

        try:
            if msg_type == ClientMsg.DECLARE:
                await self._handle_declare(conn, msg)
            elif msg_type == ClientMsg.PING:
                await self._handle_ping(conn)
            elif msg_type == ClientMsg.FORFEIT:
                await self._handle_forfeit(conn)
            elif msg_type == ClientMsg.JOIN:
                pass  # already handled via handle_join
            else:
                await conn.send_json(
                    msg_error(f"Unknown message type: "
                              f"'{msg_type}'"))
        except Exception as e:
            tb = traceback.format_exc()
            await conn.send_json(
                msg_error(f"Server error handling "
                          f"'{msg_type}': {e}"))
            print(f"[BattleHandler] Error in session "
                  f"{self.session_id}:\n{tb}")

    # ── Disconnect ────────────────────────────

    async def handle_disconnect(self,
                                conn: WebSocketConnection):
        await self._cm.unregister(
            self.session_id, conn.player_id)
        await self._sm.disconnect_player(
            self.session_id, conn.player_id)

        await self._cm.broadcast(
            self.session_id,
            _msg("opponent_disconnected",
                 player_id=conn.player_id,
                 message=f"Player {conn.player_id} "
                         f"disconnected."),
            exclude_pid=conn.player_id)

    # ── Ping ──────────────────────────────────

    async def _handle_ping(self,
                           conn: WebSocketConnection):
        await conn.send_json(msg_pong())

    # ── Forfeit ───────────────────────────────

    async def _handle_forfeit(self,
                              conn: WebSocketConnection):
        forfeiter = conn.player_id
        winner    = 2 if forfeiter == 1 else 1

        rec = await self._sm.get_session(self.session_id)
        async with rec.lock:
            rec.forfeit_player = forfeiter
            rec.touch()

            state = await self._db.load_session(
                self.session_id)
            if not self._db.is_battle_over(state):
                self._db.set_battle_over(state, winner)
                self._db.set_phase(state, GamePhase.GAME_OVER)
                self._db.append_log(state, [
                    f"Player {forfeiter} forfeited. "
                    f"Player {winner} wins!"])
                await self._db.save_session(
                    self.session_id, state)

            state    = await self._db.load_session(
                self.session_id)
            snapshot = self._db.build_render_snapshot(state)

        await self._cm.broadcast(
            self.session_id,
            msg_game_over(winner, snapshot))

    # ── Declare ───────────────────────────────

    async def _handle_declare(self,
                              conn: WebSocketConnection,
                              payload: dict):
        valid, reason, cleaned = validate_declaration(payload)
        if not valid:
            await conn.send_json(
                msg_error(f"Invalid declaration: {reason}"))
            return

        pid = cleaned["player_id"]
        if pid != conn.player_id:
            await conn.send_json(
                msg_error("player_id does not match "
                          "connection."))
            return

        rec = await self._sm.get_session(self.session_id)
        async with rec.lock:
            state = await self._db.load_session(
                self.session_id)
            if self._db.is_battle_over(state):
                await conn.send_json(
                    msg_error("Battle is already over."))
                return

            if rec.player_declared[pid]:
                await conn.send_json(
                    msg_error("You have already declared "
                              "this turn."))
                return

            # Write declaration
            state["declarations"][f"p{pid}"] = {
                "move_name"    : cleaned["move_name"],
                "action_order" : cleaned["action_order"],
                "target_tile"  : cleaned["target_tile"],
                "planned_path" : cleaned["planned_path"],
            }
            state["pokemon"][f"p{pid}"]["declaration"] = \
                state["declarations"][f"p{pid}"]
            await self._db.save_session(
                self.session_id, state)

            rec.set_declared(pid)
            both_declared = rec.both_declared

            # Notify opponent
            await self._cm.broadcast(
                self.session_id,
                msg_declared(pid),
                exclude_pid=pid)

        # Resolve outside the lock
        if both_declared:
            await self._resolve_and_broadcast()

    # ── Turn start ────────────────────────────

    async def _start_turn(self):
        state = await self._db.load_session(self.session_id)
        self._db.increment_turn(state)
        self._db.set_phase(state, GamePhase.DECLARING)
        self._db.clear_declarations(state)
        state["last_turn_results"] = {"p1": None, "p2": None}

        turn = state["meta"]["turn_number"]
        self._db.append_log(state,
                            [f"═══ Turn {turn} ═══"])
        self._db.tick_cooldowns_in_state(state)

        # Log stat stages if any are non-zero
        for key in ("p1", "p2"):
            pdata = state["pokemon"][key]
            stages = pdata.get("stat_stages", {})
            non_zero = {k: v for k, v in stages.items() if v != 0}
            if non_zero:
                stage_str = ", ".join(f"{k} {v:+d}" for k, v in non_zero.items())
                self._db.append_log(state, [
                    f"  {pdata['name']} (P{pdata['player_id']}) "
                    f"stat stages: {stage_str}"])

        await self._db.save_session(self.session_id, state)
        await self._sm.clear_declarations(self.session_id)

        snapshot = self._db.build_render_snapshot(state)
        await self._cm.broadcast(
            self.session_id,
            msg_turn_start(
                state["meta"]["turn_number"], snapshot))

    # ── Resolution ────────────────────────────

    async def _resolve_and_broadcast(self):
        # ── Resolve ──────────────────────────
        state = await self._db.load_session(self.session_id)
        if self._db.is_battle_over(state):
            return

        p1 = self._db.load_pokemon(state, 1)
        p2 = self._db.load_pokemon(state, 2)
        tm = self._db.load_terrain(state)

        self._te._log_lines       = []
        self._te._move_results    = {}
        self._te._aoe_tiles       = {}
        self._te.resolved_actions = []

        self._te._log("── Resolution phase ──")
        queue = self._te._build_queue(p1, p2)

        self._te._log("Priority order:")
        for i, a in enumerate(queue):
            self._te._log(
                f"  {i+1}. P{a.player_id} "
                f"{a.action_type} (pri={a.priority})")

        shim = _InlineDB(self._db, state)

        for action in queue:
            if self._te._determine_winner(p1, p2) != -1:
                break
            other = p2 if action.player_id == 1 else p1

            if action.action_type == ActionType.MOVE:
                move_def = MOVE_REGISTRY[
                    action.pokemon.declaration.move_name]
                if (move_def.quick_priority
                        and action.pokemon.declaration
                        .action_order
                        == ActionOrder.MOVE_FIRST):
                    override = qa_effective_move_range(
                        action.pokemon.effective_speed)
                    self._te._log(
                        f"  QA move-first: boosted range = "
                        f"{override} tiles.")
                    self._te._resolve_move_action(
                        action, tm, other, state, shim,
                        override_range=override)
                else:
                    self._te._resolve_move_action(
                        action, tm, other, state, shim)

            elif action.action_type == ActionType.ATTACK:
                self._te._resolve_attack_action(
                    action, tm, other, state, shim)

        self._db.save_pokemon(state, p1)
        self._db.save_pokemon(state, p2)
        self._db.save_terrain(state, tm)
        self._db.save_move_results(
            state, self._te._move_results)
        self._db.append_log(state, self._te._log_lines)

        winner = self._te._determine_winner(p1, p2)
        if winner != -1:
            self._db.set_battle_over(state, winner)
            self._db.set_phase(state, GamePhase.GAME_OVER)
            if winner is not None:
                self._db.append_log(
                    state, [f"🏆 Player {winner} wins!"])
            else:
                self._db.append_log(state, ["🤝 Draw!"])

        await self._db.save_session(self.session_id, state)

        # ── Broadcast RESOLUTION ─────────────
        snapshot       = self._db.build_render_snapshot(state)
        resolved_dicts = self._te.get_resolved_actions_dicts()
        recent_log     = self._db.get_recent_log(state, n=20)

        await self._cm.broadcast(
            self.session_id,
            msg_resolution(resolved_dicts, snapshot,
                           recent_log))

        if self._db.is_battle_over(state):
            await self._cm.broadcast(
                self.session_id,
                msg_game_over(
                    state["meta"]["winner"], snapshot))
            return

        # ── End turn ─────────────────────────
        state = await self._db.load_session(self.session_id)
        tm2   = self._db.load_terrain(state)
        expired = tm2.tick_all()
        for tile, name in expired:
            self._db.append_log(
                state,
                [f"  Terrain '{name}' at {tile} expired."])
        self._db.save_terrain(state, tm2)
        self._db.clear_per_turn_fields(state)
        self._db.clear_declarations(state)

        if not self._db.is_battle_over(state):
            p1d = state["pokemon"]["p1"]
            p2d = state["pokemon"]["p2"]

            def cd_str(pd):
                active = {k: v for k, v in
                          pd.get("cooldowns", {}).items()
                          if v > 0}
                return f" CD:{active}" if active else ""

            def stage_str(pd):
                stages = pd.get("stat_stages", {})
                non_zero = {k: v for k, v in stages.items() if v != 0}
                if non_zero:
                    return " STG:" + ",".join(f"{k}{v:+d}" for k, v in non_zero.items())
                return ""

            self._db.append_log(state, [
                f"End T{state['meta']['turn_number']}  "
                f"P1 HP:{p1d['current_hp']}/{p1d['max_hp']}"
                f"@{p1d['tile']}{cd_str(p1d)}{stage_str(p1d)}  |  "
                f"P2 HP:{p2d['current_hp']}/{p2d['max_hp']}"
                f"@{p2d['tile']}{cd_str(p2d)}{stage_str(p2d)}"])

        await self._db.save_session(self.session_id, state)

        # Start next turn
        await self._start_turn()


# ─────────────────────────────────────────────
# HANDLER FACTORY
# ─────────────────────────────────────────────

async def handle_websocket(
    ws,
    session_id : str,
    player_id  : int,
    db         : "DatabaseLayer",
    sm         : "SessionManager",
    cm         : "ConnectionManager",
    te         : "TurnEngine",
):
    conn    = WebSocketConnection(ws, session_id, player_id)
    handler = BattleHandler(session_id, db, sm, cm, te)

    try:
        await ws.accept()
        await handler.handle_join(conn, player_id)

        while conn.connected:
            msg = await conn.receive_json()
            if msg is None:
                break
            await handler.handle_message(conn, msg)

    except Exception as e:
        print(f"[handle_websocket] Unhandled error "
              f"session={session_id} player={player_id}: {e}")
        traceback.print_exc()

    finally:
        await handler.handle_disconnect(conn)
        await conn.close()

