from typing import Optional

# ─────────────────────────────────────────────
# ACTION TYPES
# ─────────────────────────────────────────────

class ActionType(str):
    MOVE   = "move"
    ATTACK = "attack"


# ─────────────────────────────────────────────
# QUEUED ACTION
# ─────────────────────────────────────────────

@dataclass
class QueuedAction:
    priority    : int
    player_id   : int
    action_type : str
    pokemon     : "Pokemon"
    tiebreak    : float = field(default_factory=random.random)

    def sort_key(self):
        return (-self.priority, self.tiebreak)

    def __repr__(self):
        return (f"QueuedAction(P{self.player_id} "
                f"{self.action_type} pri={self.priority})")


# ─────────────────────────────────────────────
# RESOLVED ACTION  (for animation broadcast)
# ─────────────────────────────────────────────

@dataclass
class ResolvedAction:
    player_id    : int
    action_type  : str
    move_name    : Optional["MoveName"]    = None
    action_order : Optional["ActionOrder"] = None
    from_tile    : Optional[tuple]         = None
    to_tile      : Optional[tuple]         = None
    path         : list                    = field(default_factory=list)
    result       : Optional["MoveResult"]  = None
    qa_landed_at : Optional[tuple]         = None
    stat_changes : list                    = field(default_factory=list)

    def to_dict(self) -> dict:
        return {
            "player_id"    : self.player_id,
            "action_type"  : self.action_type,
            "move_name"    : (self.move_name.value
                              if self.move_name is not None
                              else None),
            "action_order" : (self.action_order.value
                              if self.action_order is not None
                              else None),
            "from_tile"    : (list(self.from_tile)
                              if self.from_tile is not None
                              else None),
            "to_tile"      : (list(self.to_tile)
                              if self.to_tile is not None
                              else None),
            "path"         : [list(t) for t in self.path],
            "result"       : (self.result.to_dict()
                              if self.result is not None
                              else None),
            "qa_landed_at" : (list(self.qa_landed_at)
                              if self.qa_landed_at is not None
                              else None),
            "stat_changes" : list(self.stat_changes),
        }


# ─────────────────────────────────────────────
# TURN ENGINE
# ─────────────────────────────────────────────

class TurnEngine:

    def __init__(self):
        self._log_lines       : list = []
        self._move_results    : dict = {}
        self._aoe_tiles       : dict = {}
        self.resolved_actions : list = []

    # ── Logging ───────────────────────────────

    def _log(self, line: str):
        self._log_lines.append(line)

    def _log_all(self, lines: list):
        self._log_lines.extend(lines)

    # ── Queue construction ────────────────────

    def _build_queue(self, p1: "Pokemon",
                     p2: "Pokemon") -> list:
        queue = []

        for pokemon in (p1, p2):
            if not pokemon.is_alive or pokemon.declaration is None:
                continue

            decl     = pokemon.declaration
            move_def = MOVE_REGISTRY[decl.move_name]
            mp, ap   = pokemon.get_action_priorities()

            if move_def.quick_priority:
                order = decl.action_order

                if order == ActionOrder.ATTACK_FIRST:
                    queue.append(QueuedAction(
                        priority=qa_attack_priority(
                            pokemon.effective_speed,
                            ActionOrder.ATTACK_FIRST),
                        player_id=pokemon.player_id,
                        action_type=ActionType.ATTACK,
                        pokemon=pokemon))

                else:  # MOVE_FIRST
                    boosted_p = qa_attack_priority(
                        pokemon.effective_speed,
                        ActionOrder.MOVE_FIRST)
                    queue.append(QueuedAction(
                        priority=boosted_p,
                        player_id=pokemon.player_id,
                        action_type=ActionType.MOVE,
                        pokemon=pokemon))
                    queue.append(QueuedAction(
                        priority=boosted_p,
                        player_id=pokemon.player_id,
                        action_type=ActionType.ATTACK,
                        pokemon=pokemon))

            else:
                queue.append(QueuedAction(
                    priority=mp,
                    player_id=pokemon.player_id,
                    action_type=ActionType.MOVE,
                    pokemon=pokemon))
                queue.append(QueuedAction(
                    priority=ap,
                    player_id=pokemon.player_id,
                    action_type=ActionType.ATTACK,
                    pokemon=pokemon))

        queue.sort(key=lambda a: a.sort_key())
        return queue

    # ── Path truncation with step costs ───────

    def _truncate_path(self, path: list, blocked_tiles: set,
                       occupied_tiles: set, can_pass: bool,
                       move_range: int,
                       terrain_manager: "TerrainManager") -> list:
        """
        Walk declared path, stop when:
          - tile is blocked
          - tile is occupied (and can't pass)
          - cumulative step cost exceeds move_range
        Returns at least the start tile.
        """
        result = [path[0]]
        cumulative_cost = 0

        for tile in path[1:]:
            if tile in blocked_tiles:
                break
            if not can_pass and tile in occupied_tiles:
                break
            # Get step cost for this tile
            step_cost = terrain_manager.get_step_cost(tile)
            if cumulative_cost + step_cost > move_range:
                break
            cumulative_cost += step_cost
            result.append(tile)

        return result

    # ── Stat hazard application during movement ─

    def _apply_movement_stat_hazards(self, pokemon, actual_path,
                                      terrain_manager, state, db):
        """
        Check each tile in the movement path for stat hazard terrain.
        Apply stat stage changes on first entry into each contiguous
        field (entry penalty: once per field, not per tile).
        Returns list of (stat_name, delta, new_stage) applied.
        """
        changes = []
        visited_fields = []  # list of sets — each set is one contiguous field we've entered

        for tile in actual_path[1:]:  # skip start tile
            effect = terrain_manager.get_stat_effect(tile)
            if effect is None:
                continue

            # Check if this tile belongs to a field we've already entered
            already_entered = False
            for field_set in visited_fields:
                if tile in field_set:
                    already_entered = True
                    break

            if already_entered:
                continue

            # New field entry — find the full contiguous field
            entity = terrain_manager.get_terrain(tile)
            if entity is None:
                continue
            field_tiles = terrain_manager.get_contiguous_field(tile, entity.terrain_type)
            visited_fields.append(field_tiles)

            # Apply stat effects
            for stat_name, delta in effect.items():
                new_stage, was_clamped = pokemon.apply_stat_stage(stat_name, delta)
                # Also update state dict
                db.apply_stat_stage_in_state(state, pokemon.player_id, stat_name, delta)
                changes.append((stat_name, delta, new_stage))

                if was_clamped:
                    self._log(f"  ⚡ {pokemon.name} (P{pokemon.player_id}) "
                              f"{stat_name} can't go any {'lower' if delta < 0 else 'higher'}! "
                              f"(stage {new_stage:+d})")
                else:
                    direction = "fell" if delta < 0 else "rose"
                    mult = stat_stage_multiplier(new_stage)
                    self._log(f"  ⚡ {pokemon.name} (P{pokemon.player_id}) "
                              f"entered web field! Speed {direction}! "
                              f"(stage {new_stage:+d}, ×{mult:.2f})")

        return changes

    # ── Movement resolver ─────────────────────

    def _resolve_move_action(
        self,
        action          : QueuedAction,
        terrain_manager : "TerrainManager",
        other           : "Pokemon",
        state           : dict,
        db,
        override_range  : Optional[int] = None,
    ):
        pokemon = action.pokemon
        decl    = pokemon.declaration

        if not pokemon.is_alive:
            self._log(
                f"  P{pokemon.player_id} fainted — movement skipped.")
            return

        path = decl.planned_path
        if not path or len(path) < 2:
            self._log(
                f"  {pokemon.name} (P{pokemon.player_id}) "
                f"stays in place.")
            pokemon.path_this_turn = [pokemon.tile]
            self.resolved_actions.append(ResolvedAction(
                player_id=pokemon.player_id,
                action_type=ActionType.MOVE,
                from_tile=pokemon.tile,
                to_tile=pokemon.tile,
                path=[pokemon.tile]))
            return

        # Re-read blocked tiles at execution time
        blocked    = terrain_manager.blocked_movement_tiles
        occupied   = {other.tile} if other.is_alive else set()
        move_range = (override_range
                      if override_range is not None
                      else pokemon.effective_move_range)

        # Truncate path using step costs
        actual_path = self._truncate_path(
            path, blocked, occupied, pokemon.can_pass_through,
            move_range, terrain_manager)

        destination    = actual_path[-1]
        actual_tiles   = len(actual_path) - 1
        intended_tiles = len(path) - 1
        from_tile      = actual_path[0]

        # Calculate actual step cost spent
        actual_cost = sum(
            terrain_manager.get_step_cost(t)
            for t in actual_path[1:]
        )

        was_stopped_early = (
            actual_tiles < intended_tiles
            and actual_cost < move_range)

        pokemon.path_this_turn = actual_path

        if actual_tiles > 0:
            pokemon.move_to(destination)
            self._log(
                f"  {pokemon.name} (P{pokemon.player_id}) moved "
                f"{actual_tiles} tile(s) (cost {actual_cost}) → {destination}.")
        else:
            self._log(
                f"  {pokemon.name} (P{pokemon.player_id}) "
                f"blocked — cannot move.")

        # Apply stat hazards from terrain along path
        stat_changes = self._apply_movement_stat_hazards(
            pokemon, actual_path, terrain_manager, state, db)

        # Early-stop boost (not for momentum moves)
        move_def = MOVE_REGISTRY[decl.move_name]
        if was_stopped_early and not move_def.needs_momentum:
            pokemon.apply_early_stop(intended_tiles, actual_tiles)
            db.set_pokemon_boost(
                state, pokemon.player_id,
                pokemon.boost_mult, pokemon.tiles_short)
            self._log(
                f"  ⚡ Early stop! {pokemon.name} blocked after "
                f"{actual_tiles}/{intended_tiles} tiles. "
                f"Attack boost ×{pokemon.boost_mult:.2f}.")

        # Straight-line approach count (momentum)
        target = decl.target_tile
        if target is not None and actual_tiles > 0:
            direction = get_attack_direction(
                *destination, *target)
            straight = count_straight_approach(
                actual_path, direction)
            pokemon.straight_tiles_this_turn = straight
        else:
            pokemon.straight_tiles_this_turn = 0

        self.resolved_actions.append(ResolvedAction(
            player_id=pokemon.player_id,
            action_type=ActionType.MOVE,
            from_tile=from_tile,
            to_tile=destination,
            path=actual_path,
            stat_changes=stat_changes))

    # ── Attack resolver ───────────────────────

    def _resolve_attack_action(
        self,
        action          : QueuedAction,
        terrain_manager : "TerrainManager",
        other           : "Pokemon",
        state           : dict,
        db,
    ):
        pokemon  = action.pokemon
        defender = other
        decl     = pokemon.declaration

        if not pokemon.is_alive:
            self._log(
                f"  P{pokemon.player_id} fainted — attack skipped.")
            return

        move_def    = MOVE_REGISTRY[decl.move_name]
        target_tile = decl.target_tile
        tiles_moved = decl.tiles_to_move()

        # Volt Tackle: force boost_mult = 1.0
        if move_def.needs_momentum:
            pokemon.boost_mult = 1.0

        self._log(
            f"  {pokemon.name} (P{pokemon.player_id}) "
            f"uses {move_def.name.value}"
            + (f" → {target_tile}"
               if target_tile else " (auto-target)"))

        result = execute_move(
            move_def        = move_def,
            attacker        = pokemon,
            defender        = defender,
            target_tile     = target_tile,
            straight_tiles  = pokemon.straight_tiles_this_turn,
            terrain_manager = terrain_manager,
            tiles_moved     = tiles_moved,
            action_order    = decl.action_order)

        self._log_all(result.log_lines)
        self._move_results[pokemon.player_id] = result
        self._aoe_tiles[pokemon.player_id]    = result.tiles_hit

        # Apply cooldown to both Pokemon object and state dict
        pokemon.apply_cooldown(decl.move_name, decl.action_order)
        db.apply_cooldown_in_state(
            state, pokemon.player_id,
            decl.move_name, decl.action_order)

        cd_turns = (COOLDOWN_ATTACK_FIRST
                    if decl.action_order == ActionOrder.ATTACK_FIRST
                    else COOLDOWN_MOVE_FIRST)
        self._log(
            f"  🕐 {move_def.name.value} cooldown: "
            f"{cd_turns} turn(s).")

        if not defender.is_alive:
            self._log(
                f"  ⚡ {defender.name} "
                f"(P{defender.player_id}) fainted!")
        if not pokemon.is_alive:
            self._log(
                f"  ⚡ {pokemon.name} "
                f"(P{pokemon.player_id}) fainted!")

        self.resolved_actions.append(ResolvedAction(
            player_id=pokemon.player_id,
            action_type=ActionType.ATTACK,
            move_name=decl.move_name,
            action_order=decl.action_order,
            from_tile=pokemon.tile,
            to_tile=defender.tile,
            result=result,
            qa_landed_at=result.qa_landed_at))

    # ── Win condition ─────────────────────────

    def _determine_winner(self, p1: "Pokemon",
                          p2: "Pokemon") -> Optional[int]:
        if p1.is_alive and p2.is_alive:
            return -1
        if not p1.is_alive and not p2.is_alive:
            return None
        if not p1.is_alive:
            return 2
        return 1

    # ── Full turn lifecycle ───────────────────

    def begin_turn(self, db: "SyncDatabaseLayer",
                   session_id: str):
        state = db.load(session_id)
        db.increment_turn(state)
        db.set_phase(state, GamePhase.DECLARING)
        db.clear_declarations(state)
        state["last_turn_results"] = {"p1": None, "p2": None}

        turn = state["meta"]["turn_number"]
        db.append_log(state, [f"═══ Turn {turn} ═══"])

        # Tick cooldowns
        db.tick_cooldowns_in_state(state)

        # Log stat stages if any are non-zero
        for key in ("p1", "p2"):
            pdata = state["pokemon"][key]
            stages = pdata.get("stat_stages", {})
            non_zero = {k: v for k, v in stages.items() if v != 0}
            if non_zero:
                stage_str = ", ".join(f"{k} {v:+d}" for k, v in non_zero.items())
                db.append_log(state, [
                    f"  {pdata['name']} (P{pdata['player_id']}) "
                    f"stat stages: {stage_str}"])

        db.save(session_id, state)

    def resolve_turn(self, db: "SyncDatabaseLayer",
                     session_id: str):
        state = db.load(session_id)
        if db.is_battle_over(state):
            return

        p1 = db.load_pokemon(state, 1)
        p2 = db.load_pokemon(state, 2)
        tm = db.load_terrain(state)

        self._log_lines       = []
        self._move_results    = {}
        self._aoe_tiles       = {}
        self.resolved_actions = []

        self._log("── Resolution phase ──")
        queue = self._build_queue(p1, p2)

        self._log("Priority order:")
        for i, a in enumerate(queue):
            self._log(
                f"  {i+1}. P{a.player_id} "
                f"{a.action_type} (pri={a.priority})")

        for action in queue:
            if self._determine_winner(p1, p2) != -1:
                break

            other = p2 if action.player_id == 1 else p1

            if action.action_type == ActionType.MOVE:
                move_def = MOVE_REGISTRY[
                    action.pokemon.declaration.move_name]

                if (move_def.quick_priority
                        and action.pokemon.declaration.action_order
                        == ActionOrder.MOVE_FIRST):
                    override = qa_effective_move_range(
                        action.pokemon.effective_speed)
                    self._log(
                        f"  QA move-first: boosted range = "
                        f"{override} tiles.")
                    self._resolve_move_action(
                        action, tm, other, state, db,
                        override_range=override)
                else:
                    self._resolve_move_action(
                        action, tm, other, state, db)

            elif action.action_type == ActionType.ATTACK:
                self._resolve_attack_action(
                    action, tm, other, state, db)

        # Persist all mutations
        db.save_pokemon(state, p1)
        db.save_pokemon(state, p2)
        db.save_terrain(state, tm)
        db.save_move_results(state, self._move_results)
        db.append_log(state, self._log_lines)

        # Final win check
        winner = self._determine_winner(p1, p2)
        if winner != -1:
            db.set_battle_over(state, winner)
            db.set_phase(state, GamePhase.GAME_OVER)
            if winner is not None:
                db.append_log(state,
                              [f"🏆 Player {winner} wins!"])
            else:
                db.append_log(state, ["🤝 Draw!"])

        db.save(session_id, state)

    def end_turn(self, db: "SyncDatabaseLayer",
                 session_id: str):
        state = db.load(session_id)

        tm      = db.load_terrain(state)
        expired = tm.tick_all()
        for tile, name in expired:
            db.append_log(state,
                          [f"  Terrain '{name}' at {tile} expired."])

        db.save_terrain(state, tm)
        db.clear_per_turn_fields(state)
        db.clear_declarations(state)

        if not db.is_battle_over(state):
            p1d = state["pokemon"]["p1"]
            p2d = state["pokemon"]["p2"]

            def cd_str(pdata):
                active = {
                    k: v for k, v in
                    pdata.get("cooldowns", {}).items()
                    if v > 0
                }
                return f" CD:{active}" if active else ""

            def stage_str(pdata):
                stages = pdata.get("stat_stages", {})
                non_zero = {k: v for k, v in stages.items() if v != 0}
                if non_zero:
                    return " STG:" + ",".join(f"{k}{v:+d}" for k, v in non_zero.items())
                return ""

            db.append_log(state, [
                f"End T{state['meta']['turn_number']}  "
                f"P1 HP:{p1d['current_hp']}/{p1d['max_hp']}"
                f"@{p1d['tile']}{cd_str(p1d)}{stage_str(p1d)}  |  "
                f"P2 HP:{p2d['current_hp']}/{p2d['max_hp']}"
                f"@{p2d['tile']}{cd_str(p2d)}{stage_str(p2d)}"])

        db.save(session_id, state)

    def run_full_turn(self, db: "SyncDatabaseLayer",
                      session_id: str):
        self.begin_turn(db, session_id)
        self.resolve_turn(db, session_id)
        self.end_turn(db, session_id)

    def get_last_aoe_tiles(self) -> dict:
        return {pid: set(tiles)
                for pid, tiles in self._aoe_tiles.items()}

    def get_resolved_actions_dicts(self) -> list:
        return [ra.to_dict() for ra in self.resolved_actions]



