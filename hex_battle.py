# ─────────────────────────────────────────────
# MOVE VALIDATION
# ─────────────────────────────────────────────

def validate_move(move_def, attacker, target_tile, straight_tiles, barrier_tiles, grid=None):
    if grid is None: grid = HEX_GRID
    if target_tile not in grid:     return False, "Target tile outside battlefield."
    if not attacker.is_alive:       return False, "Attacker has fainted."
    if not move_def.is_ranged:
        dist = hex_distance(*attacker.tile, *target_tile)
        if dist != 1: return False, f"{move_def.name.value} is melee — must be adjacent (distance={dist})."
    if move_def.requires_los and not move_def.bypasses_los:
        los_clear, _ = has_line_of_sight(*attacker.tile, *target_tile, barrier_tiles, grid)
        if not los_clear: return False, "Line of sight blocked by a barrier."
    if move_def.needs_momentum and straight_tiles < 1:
        return False, f"{move_def.name.value} requires ≥1 straight tile of approach."
    return True, "OK"


# ─────────────────────────────────────────────
# MOVE EXECUTION
# ─────────────────────────────────────────────

def execute_move(move_def, attacker, defender, target_tile,
                 straight_tiles, terrain_manager,
                 tiles_moved=0, action_order=None, grid=None):
    if grid is None: grid = HEX_GRID

    result    = MoveResult()
    move_name = move_def.name.value

    # ── QA move-first: auto-target ────────────
    if (move_def.name == MoveName.QUICK_ATTACK
            and action_order == ActionOrder.MOVE_FIRST):
        barrier_tiles = terrain_manager.blocked_movement_tiles
        can_reach, _  = qa_check_straight_line(
            attacker.tile, defender.tile, barrier_tiles, grid)
        if not can_reach:
            result.qa_failed   = True
            result.miss_reason = "Quick Attack (move-first) failed — barrier on straight line."
            result.add_log(f"{attacker.name} (P{attacker.player_id}) used {move_name}! FAILED — barrier blocks path.")
            return result
        target_tile = defender.tile
        result.add_log(f"{attacker.name} (P{attacker.player_id}) used {move_name}! Dashes toward opponent at {target_tile}.")

    # ── Standard validation ───────────────────
    else:
        if target_tile is None:
            result.miss_reason = "No target tile declared."
            result.add_log(f"{attacker.name} (P{attacker.player_id}): {move_name} failed — no target.")
            return result
        barrier_tiles = terrain_manager.blocked_los_tiles
        can_fire, reason = validate_move(move_def, attacker, target_tile, straight_tiles, barrier_tiles, grid)
        if not can_fire:
            result.miss_reason = reason
            result.add_log(f"{attacker.name} (P{attacker.player_id}): {move_name} failed — {reason}")
            return result

    # ── Multipliers ───────────────────────────
    result.momentum_mult = momentum_multiplier(straight_tiles) if move_def.needs_momentum else 1.0
    result.power_mult = qa_power_multiplier(tiles_moved, action_order) if move_def.name == MoveName.QUICK_ATTACK else 1.0
    result.boost_mult = 1.0 if move_def.needs_momentum else attacker.boost_mult
    result.type_mult  = get_move_type_mult(move_def, defender)

    # ── AOE tiles ─────────────────────────────
    aoe_tiles        = resolve_aoe(move_def, attacker.tile, target_tile, grid)
    result.tiles_hit = aoe_tiles

    # Build log annotation
    mult_parts = []
    if result.momentum_mult != 1.0: mult_parts.append(f"Momentum ×{result.momentum_mult:.2f}")
    if result.power_mult != 1.0:
        label = "AF boost" if action_order == ActionOrder.ATTACK_FIRST else "MF penalty"
        mult_parts.append(f"{label} ×{result.power_mult:.2f}")
    if result.boost_mult > 1.0: mult_parts.append(f"Early-stop ×{result.boost_mult:.2f}")
    if result.type_mult != 1.0:
        if result.type_mult > 1.0:     mult_parts.append(f"Super effective ×{result.type_mult:.1f}")
        elif result.type_mult == 0.0:  mult_parts.append("No effect!")
        else:                          mult_parts.append(f"Not very effective ×{result.type_mult:.1f}")
    mult_str = (" | " + " | ".join(mult_parts)) if mult_parts else ""

    if not (move_def.name == MoveName.QUICK_ATTACK and action_order == ActionOrder.MOVE_FIRST):
        result.add_log(f"{attacker.name} (P{attacker.player_id}) used {move_name}! Target: {target_tile} | AOE: {len(aoe_tiles)} tile(s){mult_str}")

    # ── Terrain destruction (physical moves) ──
    if move_def.category == MoveCategory.PHYSICAL:
        for tile in aoe_tiles:
            if terrain_manager.has_terrain(tile):
                destroyed = terrain_manager.damage_terrain_at(tile, amount=1)
                for name in destroyed:
                    result.terrain_destroyed.append((tile, name))
                    result.add_log(f"  Terrain '{name}' at {tile} destroyed!")

    # ── Electro Web: place slow zone terrain ──
    # The terrain carries stat_effect metadata. Actual stat stage
    # application happens when a Pokemon enters the field (Cell 6).
    if move_def.name == MoveName.ELECTRO_WEB:
        placed_count = 0
        for tile in aoe_tiles:
            existing = terrain_manager.get_terrain(tile)
            if existing and existing.is_permanent:
                continue  # don't overwrite rock/tree
            terrain_manager.add_terrain(
                tile=tile, terrain_type=TerrainType.SLOW_ZONE,
                owner=attacker.player_id,
                duration=TERRAIN_DURATION_MODERATE, integrity=1)
            result.terrain_placed.append((tile, TerrainType.SLOW_ZONE.value))
            placed_count += 1
        result.add_log(f"  🕸️ Electro Web placed web terrain on {placed_count} tile(s)!")
        result.add_log(f"  ⚡ Entering the web field applies {ELECTRO_WEB_SPEED_STAGES} Speed stage(s).")

    # ── No-effect check ───────────────────────
    if result.type_mult == 0.0:
        result.miss_reason = "No effect (type immunity)."
        result.add_log(f"  It had no effect on {defender.name}!")
        return result

    # ── Hit check ─────────────────────────────
    if not defender.is_alive:
        result.miss_reason = "Defender already fainted."
        result.add_log(f"  {defender.name} already fainted.")
    elif defender.tile in aoe_tiles:
        dmg = calculate_damage(move_def, attacker, defender,
                               momentum=result.momentum_mult, power_mult=result.power_mult,
                               boost_mult=result.boost_mult, type_mult=result.type_mult)
        actual = defender.take_damage(dmg)
        result.hit          = True
        result.damage_dealt = actual
        if move_def.category == MoveCategory.PHYSICAL:
            stat_a = int(attacker.attack * attacker.stat_stage_attack_mult)
            stat_d = int(defender.defense * defender.stat_stage_defense_mult)
        else:
            stat_a = int(attacker.sp_atk * attacker.stat_stage_spatk_mult)
            stat_d = int(defender.sp_def * defender.stat_stage_spdef_mult)
        total = result.total_multiplier()
        result.add_log(
            f"  HIT! {defender.name} (P{defender.player_id}) took {actual} dmg "
            f"[A={stat_a} D={stat_d}{f' total×{total:.2f}' if abs(total - 1.0) > 0.01 else ''}]. "
            f"HP: {defender.current_hp}/{defender.max_hp}")
        if not defender.is_alive:
            result.add_log(f"  ⚡ {defender.name} fainted!")
    else:
        result.miss_reason = f"{defender.name} not in AOE (on {defender.tile}, AOE: {len(aoe_tiles)} tile(s))."
        result.add_log(f"  Miss — {result.miss_reason}")

    # ── QA landing ────────────────────────────
    if move_def.name == MoveName.QUICK_ATTACK:
        blocked  = terrain_manager.blocked_movement_tiles
        occupied = {defender.tile}
        landing  = qa_landing_tile(attacker.tile, defender.tile, blocked, occupied, grid)
        if landing:
            attacker.move_to(landing)
            result.qa_landed_at = landing
            result.add_log(f"  {attacker.name} lands at {landing} adjacent to opponent.")
        else:
            result.add_log(f"  {attacker.name} could not find adjacent landing tile.")

    return result


