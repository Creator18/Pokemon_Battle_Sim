@dataclass
class TurnDeclaration:
    """
    Everything a player commits to during declaration phase.

    move_name      : MoveName enum
    action_order   : ATTACK_FIRST or MOVE_FIRST
    target_tile    : (q, r) or None (QA move-first: auto at resolution)
    planned_path   : list of (q, r) including start tile
    locked         : True once confirmed — immutable
    """
    move_name    : "MoveName"
    action_order : "ActionOrder"
    target_tile  : Optional[tuple]
    planned_path : list  = field(default_factory=list)
    locked       : bool  = False

    def lock(self):
        self.locked = True

    def tiles_to_move(self) -> int:
        """Number of movement steps in the declared path."""
        return max(0, len(self.planned_path) - 1)

    # ── Serialization ────────────────────────

    def to_dict(self) -> dict:
        return {
            "move_name"    : self.move_name.value,
            "action_order" : self.action_order.value,
            "target_tile"  : (list(self.target_tile)
                              if self.target_tile is not None else None),
            "planned_path" : [list(t) for t in self.planned_path],
        }

    @classmethod
    def from_dict(cls, data: dict) -> "TurnDeclaration":
        return cls(
            move_name    = MoveName(data["move_name"]),
            action_order = ActionOrder(data["action_order"]),
            target_tile  = (tuple(data["target_tile"])
                            if data.get("target_tile") is not None
                            else None),
            planned_path = [tuple(t) for t in
                            data.get("planned_path", [])],
            locked       = True,
        )

    def __repr__(self) -> str:
        return (
            f"TurnDeclaration({self.move_name.value} | "
            f"{self.action_order.value} | "
            f"target={self.target_tile} | "
            f"path_len={self.tiles_to_move()} | "
            f"locked={self.locked})"
        )


