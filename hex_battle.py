def move_summary(move_name: "MoveName") -> str:
    d = MOVE_REGISTRY[move_name]
    parts = [f"PWR {d.base_power}"]
    parts.append("Ranged" if d.is_ranged else "Melee")
    parts.append(f"AOE r{d.aoe_radius}" if d.aoe_radius > 0 else "Single")
    if d.quick_priority:  parts.append("Always First")
    if d.needs_momentum:  parts.append("Needs Momentum")
    if d.bypasses_los:    parts.append("Bypasses LoS")
    atk_type = MOVE_TYPE_MAP.get(move_name, "Normal")
    parts.append(f"Type:{atk_type}")
    return f"{move_name.value}: " + " | ".join(parts)



