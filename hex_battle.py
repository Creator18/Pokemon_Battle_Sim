# DAMAGE FORMULA + TYPE EFFECTIVENESS
# Uses stat stage multipliers for effective ATK/DEF
# ─────────────────────────────────────────────

def calculate_damage(move_def, attacker, defender,
                     momentum=1.0, power_mult=1.0,
                     boost_mult=1.0, type_mult=1.0) -> int:
    L     = attacker.level
    Power = move_def.base_power
    if move_def.category == MoveCategory.PHYSICAL:
        A = int(attacker.attack * attacker.stat_stage_attack_mult)
        D = max(1, int(defender.defense * defender.stat_stage_defense_mult))
    else:
        A = int(attacker.sp_atk * attacker.stat_stage_spatk_mult)
        D = max(1, int(defender.sp_def * defender.stat_stage_spdef_mult))
    inner = math.floor((2 * L / 5 + 2) * Power * A / D)
    base  = math.floor(inner / 50) + 2
    return max(1, math.floor(base * momentum * power_mult * boost_mult * type_mult))

def get_move_type_mult(move_def, defender) -> float:
    atk_type  = MOVE_TYPE_MAP.get(move_def.name, "Normal")
    def_types = POKEMON_TYPES.get(defender.name, ["Normal"])
    return get_type_effectiveness(atk_type, def_types)

def damage_preview(move_def, attacker, defender) -> dict:
    tm = get_move_type_mult(move_def, defender)
    base = calculate_damage(move_def, attacker, defender, type_mult=tm)
    mx   = calculate_damage(move_def, attacker, defender, momentum=MOMENTUM_CAP, type_mult=tm)
    result = {"base": base, "max_momentum": mx, "type_mult": tm}
    if move_def.name == MoveName.QUICK_ATTACK:
        max_tiles = qa_effective_move_range(attacker.base_speed)
        result["qa_af"]     = calculate_damage(move_def, attacker, defender,
                                power_mult=qa_power_multiplier(0, ActionOrder.ATTACK_FIRST), type_mult=tm)
        result["qa_mf_0t"]  = calculate_damage(move_def, attacker, defender,
                                power_mult=qa_power_multiplier(0, ActionOrder.MOVE_FIRST), type_mult=tm)
        result["qa_mf_max"] = calculate_damage(move_def, attacker, defender,
                                power_mult=qa_power_multiplier(max_tiles, ActionOrder.MOVE_FIRST), type_mult=tm)
    return result


