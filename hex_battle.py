@dataclass
class TerrainEntity:
    """One terrain entity occupying a tile."""

    tile         : tuple
    terrain_type : TerrainType
    owner        : int     = 0
    turns_left   : int     = 4
    integrity    : int     = 1

    @property
    def props(self) -> TerrainProperties:
        return TERRAIN_PROPS.get(self.terrain_type,
               TERRAIN_PROPS[TerrainType.SLOW_ZONE])

    @property
    def is_expired(self) -> bool:
        return self.turns_left <= 0

    @property
    def is_destroyed(self) -> bool:
        return self.integrity <= 0

    @property
    def is_permanent(self) -> bool:
        """ROCK and TREE are permanent (until destroyed for trees)."""
        return self.terrain_type in (TerrainType.ROCK, TerrainType.TREE)

    @property
    def blocks_movement(self) -> bool:
        return self.props.blocks_movement

    @property
    def blocks_los(self) -> bool:
        return self.props.blocks_los

    @property
    def speed_modifier(self) -> float:
        return self.props.speed_modifier

    @property
    def step_cost(self) -> int:
        """Movement steps required to enter this tile."""
        return self.props.step_cost

    @property
    def stat_effect(self) -> Optional[dict]:
        """Stat stage changes applied on field entry. None if no effect."""
        return self.props.stat_effect

    @property
    def is_stat_hazard(self) -> bool:
        """True if this terrain applies stat stage changes on entry."""
        return self.props.stat_effect is not None and len(self.props.stat_effect) > 0

    def tick(self):
        """Decrement turns_left. Permanent terrain is not ticked."""
        if not self.is_permanent:
            self.turns_left = max(0, self.turns_left - 1)

    def take_damage(self, amount: int = 1) -> bool:
        """
        Reduce integrity. Returns True if destroyed.
        ROCK is indestructible (integrity=9999).
        TREE can be destroyed (integrity=2).
        """
        if self.terrain_type == TerrainType.ROCK:
            return False   # indestructible
        self.integrity = max(0, self.integrity - amount)
        return self.integrity <= 0

    def to_dict(self) -> dict:
        return {
            "tile"         : list(self.tile),
            "terrain_type" : self.terrain_type.value,
            "owner"        : self.owner,
            "turns_left"   : self.turns_left,
            "integrity"    : self.integrity,
        }

    @classmethod
    def from_dict(cls, data: dict) -> "TerrainEntity":
        return cls(
            tile         = tuple(data["tile"]),
            terrain_type = TerrainType(data["terrain_type"]),
            owner        = int(data.get("owner", 0)),
            turns_left   = int(data.get("turns_left", 4)),
            integrity    = int(data.get("integrity", 1)),
        )

    def __repr__(self) -> str:
        return (f"Terrain({self.terrain_type.value} "
                f"@{self.tile} T={self.turns_left} "
                f"HP={self.integrity} "
                f"cost={self.step_cost})")


