class WebSocketConnection:
    """Wraps a FastAPI WebSocket for send/receive with error handling."""

    def __init__(self, ws, session_id: str, player_id: int):
        self.ws         = ws
        self.session_id = session_id
        self.player_id  = player_id
        self.connected  = True

    async def send_json(self, data: dict):
        if not self.connected:
            return
        try:
            await self.ws.send_text(json.dumps(data))
        except Exception:
            self.connected = False

    async def receive_json(self) -> Optional[dict]:
        try:
            text = await self.ws.receive_text()
            return json.loads(text)
        except Exception:
            self.connected = False
            return None

    async def close(self):
        try:
            await self.ws.close()
        except Exception:
            pass
        self.connected = False


# ─────────────────────────────────────────────
# CONNECTION MANAGER
# ─────────────────────────────────────────────

class ConnectionManager:
    """
    Registry of live WebSocket connections.
    connections[session_id][player_id] = WebSocketConnection
    """

    def __init__(self):
        self._conns: dict[str, dict[int, WebSocketConnection]] = {}
        self._lock = asyncio.Lock()

    async def register(self, conn: WebSocketConnection):
        async with self._lock:
            if conn.session_id not in self._conns:
                self._conns[conn.session_id] = {}
            self._conns[conn.session_id][conn.player_id] = conn

    async def unregister(self, session_id: str,
                         player_id: int):
        async with self._lock:
            sess = self._conns.get(session_id, {})
            sess.pop(player_id, None)
            if not sess:
                self._conns.pop(session_id, None)

    async def send_to(self, session_id: str,
                      player_id: int, data: dict):
        async with self._lock:
            conn = self._conns.get(
                session_id, {}).get(player_id)
        if conn:
            await conn.send_json(data)

    async def broadcast(self, session_id: str, data: dict,
                        exclude_pid: Optional[int] = None):
        async with self._lock:
            conns = dict(self._conns.get(session_id, {}))
        for pid, conn in conns.items():
            if exclude_pid is not None and pid == exclude_pid:
                continue
            await conn.send_json(data)

    async def connected_players(self,
                                session_id: str) -> list[int]:
        async with self._lock:
            return list(
                self._conns.get(session_id, {}).keys())

    def __repr__(self) -> str:
        total = sum(len(v) for v in self._conns.values())
        return (f"ConnectionManager({len(self._conns)} sessions, "
                f"{total} connections)")


