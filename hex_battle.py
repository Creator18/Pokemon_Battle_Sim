# SESSION MANAGER
# ─────────────────────────────────────────────

class SessionManager:
    """
    Manages all live SessionRecords in memory.
    All public methods are async and safe for concurrent use.
    """

    def __init__(self, db: "DatabaseLayer"):
        self._db       : "DatabaseLayer"          = db
        self._sessions : dict[str, SessionRecord] = {}
        self._lock     : asyncio.Lock             = asyncio.Lock()

    # ── ID generation ─────────────────────────

    @staticmethod
    def _generate_id() -> str:
        chars = string.ascii_uppercase + string.digits
        return "".join(random.choices(chars, k=SESSION_ID_LENGTH))

    async def _unique_id(self) -> str:
        for _ in range(20):
            sid = self._generate_id()
            if sid not in self._sessions:
                return sid
        raise RuntimeError(
            "Could not generate unique session ID "
            "after 20 attempts.")

    # ── Create ────────────────────────────────

    async def create_session(self) -> str:
        """Create a new session. Returns session ID."""
        async with self._lock:
            await self._cleanup_timed_out_locked()

            if len(self._sessions) >= MAX_SESSIONS:
                raise RuntimeError(
                    f"Maximum concurrent sessions "
                    f"({MAX_SESSIONS}) reached.")

            sid = await self._unique_id()
            await self._db.new_session(sid)
            self._sessions[sid] = SessionRecord(session_id=sid)
            return sid

    # ── Join ──────────────────────────────────

    async def join_session(self, session_id: str,
                           player_id: Optional[int] = None
                           ) -> int:
        """
        Join an existing session.
        Auto-assigns slot if player_id is None.
        Returns assigned player_id.
        """
        async with self._lock:
            rec = self._sessions.get(session_id)
            if rec is None:
                raise KeyError(
                    f"Session '{session_id}' not found.")

            if player_id is not None:
                if rec.player_reserved[player_id]:
                    raise ValueError(
                        f"Player {player_id} slot already taken "
                        f"in session '{session_id}'.")
                assigned = player_id
            else:
                assigned = rec.available_player_id
                if assigned is None:
                    raise ValueError(
                        f"Session '{session_id}' is full.")

            rec.player_reserved[assigned] = True
            rec.touch()
            return assigned

    # ── Disconnect ────────────────────────────

    async def disconnect_player(self, session_id: str,
                                player_id: int):
        async with self._lock:
            rec = self._sessions.get(session_id)
            if rec:
                rec.disconnect_player(player_id)

    # ── Declare ───────────────────────────────

    async def set_declared(self, session_id: str,
                           player_id: int) -> bool:
        """
        Mark player as declared. Returns True if both
        have now declared (ready to resolve).
        """
        rec = await self.get_session(session_id)
        async with rec.lock:
            rec.set_declared(player_id)
            return rec.both_declared

    async def clear_declarations(self, session_id: str):
        rec = await self.get_session(session_id)
        async with rec.lock:
            rec.clear_declarations()

    # ── Forfeit ───────────────────────────────

    async def set_forfeit(self, session_id: str,
                          player_id: int):
        rec = await self.get_session(session_id)
        async with rec.lock:
            rec.forfeit_player = player_id
            rec.touch()

    # ── Lookup ────────────────────────────────

    async def get_session(self, session_id: str
                          ) -> SessionRecord:
        async with self._lock:
            rec = self._sessions.get(session_id)
        if rec is None:
            raise KeyError(
                f"Session '{session_id}' not found.")
        return rec

    async def session_exists(self, session_id: str) -> bool:
        async with self._lock:
            return session_id in self._sessions

    async def get_all_session_ids(self) -> list[str]:
        async with self._lock:
            return list(self._sessions.keys())

    async def active_session_count(self) -> int:
        async with self._lock:
            return len(self._sessions)

    # ── Touch (keepalive) ────────────────────

    async def touch(self, session_id: str):
        async with self._lock:
            rec = self._sessions.get(session_id)
            if rec:
                rec.touch()

    # ── Removal ───────────────────────────────

    async def remove_session(self, session_id: str):
        async with self._lock:
            self._sessions.pop(session_id, None)
        try:
            await self._db.delete_session(session_id)
        except Exception:
            pass

    # ── Timeout cleanup ───────────────────────

    async def _cleanup_timed_out_locked(self):
        """Must be called while self._lock is held."""
        timed_out = [
            sid for sid, rec in self._sessions.items()
            if rec.is_timed_out(SESSION_TIMEOUT_S)
        ]
        for sid in timed_out:
            self._sessions.pop(sid, None)
            asyncio.ensure_future(
                self._db.delete_session(sid))

    async def cleanup_timed_out(self) -> list[str]:
        """Public cleanup. Returns removed session IDs."""
        async with self._lock:
            timed_out = [
                sid for sid, rec in self._sessions.items()
                if rec.is_timed_out(SESSION_TIMEOUT_S)
            ]
            for sid in timed_out:
                self._sessions.pop(sid, None)

        for sid in timed_out:
            try:
                await self._db.delete_session(sid)
            except Exception:
                pass
        return timed_out

    # ── Status ────────────────────────────────

    async def get_status(self, session_id: str) -> dict:
        """Status dict for REST endpoint."""
        rec = await self.get_session(session_id)
        status = rec.to_status_dict()
        try:
            state = await self._db.load_session(session_id)
            status["phase"]       = state["meta"]["phase"]
            status["turn_number"] = state["meta"]["turn_number"]
            status["battle_over"] = state["meta"]["battle_over"]
            status["winner"]      = state["meta"]["winner"]
        except Exception:
            pass
        return status

    async def get_manager_summary(self) -> dict:
        async with self._lock:
            return {
                "active_sessions": len(self._sessions),
                "max_sessions"   : MAX_SESSIONS,
                "sessions"       : [
                    rec.to_status_dict()
                    for rec in self._sessions.values()
                ],
            }

    def __repr__(self) -> str:
        return (f"SessionManager("
                f"{len(self._sessions)}/{MAX_SESSIONS} sessions)")



