def generate_battlefield_terrain(grid=None):
    if grid is None: grid = HEX_GRID
    terrain = []

    rock_tiles = get_hex_area(MAP_ROCK_CENTER[0], MAP_ROCK_CENTER[1], MAP_ROCK_RADIUS, grid)
    for tile in rock_tiles:
        terrain.append({
            "tile": list(tile), "terrain_type": TerrainType.ROCK.value,
            "owner": 0, "turns_left": 9999, "integrity": 9999,
        })

    excluded = set(rock_tiles)
    for spawn in [P1_START, P2_START]:
        excluded.add(spawn)
        for nb in hex_neighbors(*spawn):
            if nb in grid: excluded.add(nb)
        for nb in hex_neighbors(*spawn):
            for nb2 in hex_neighbors(*nb):
                if nb2 in grid and hex_distance(*spawn, *nb2) <= MAP_SPAWN_EXCLUSION:
                    excluded.add(nb2)

    eligible = [t for t in grid if t not in excluded]
    random.shuffle(eligible)
    tree_count = min(MAP_TREE_COUNT, len(eligible))
    tree_tiles = eligible[:tree_count]

    for tile in tree_tiles:
        terrain.append({
            "tile": list(tile), "terrain_type": TerrainType.TREE.value,
            "owner": 0, "turns_left": 9999, "integrity": MAP_TREE_INTEGRITY,
        })

    return terrain



